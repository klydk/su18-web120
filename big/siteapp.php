 <h2 class="pageID">Responsive Site v.s. Native App.</h2>
    
    
     <p>Before we are able to choose between using a responsive website and a mobile application, it is important to understand the differences between the two. Both of these, websites and applications, will of course be accessed the same way, on handheld devices such as our smartphones or tablets. Lately, developers have hit a bit of a bump in the road when deciding what approach to take when designing for these products. Each offer their own benefits, and one isn’t necessarily better than the other, it comes down to “needs”.</p>
 
        	<p>A responsive mobile website is similar to any other website that we access, it will consist of browser-based HTML pages that are linked together and accessed over our providers internet. A mobile device today, is typically run on a 4G network, some are still 3G, while others are working strictly on Wi-Fi (lots of tablets still don’t use their own networks). Mobile websites will often be housed under a different URL, for example https://m.facebook.com rather than the standard facebook.com. These pages will also look a bit different than the typical desktop site that we are used to seeing, making them appropriate for mobile viewing. The pages will adapt to the size of the user’s device. Responsive web design is becoming the new standard for websites that are not only mobile-friendly, but that can scale to any sized device- from desktop down to tablet and handheld smartphones.</p>
 
        	<p>Mobile applications, on the other hand, are actual applications that we will download and install on each of our mobile devices, rather than being rendered within a browser. Applications are customized to each Operating System, for Apple we have Apple’s App Store, then there is the Android Market, Blackberry App World, and Google Play. Some apps are only made for a particular Operating System or device, most widely used applications will be available for each OS. These applications will pull content and data from the internet, within the app.</p>
 
        	<p>So, which is better? Like I said before, it comes down to the specific needs and end goals. If you’re developing a game, an app is most likely the best option. If you’re looking to offer mobile-friendly content to the widest possible audience, then a mobile website is probably what you’re looking for. In some cases, both are needed.</p>

